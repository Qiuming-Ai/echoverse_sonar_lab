bool runPoseEditorDialog(QWidget* parent, const QString& title, std::array<double, 6>* pose) {
    if (!pose) {
        return false;
    }
    QDialog dlg(parent);
    dlg.setWindowTitle(title);
    dlg.resize(380, 300);
    auto* layout = new QVBoxLayout(&dlg);
    auto* form = new QFormLayout();
    QDoubleSpinBox* s[6];
    const QString labels[] = {QStringLiteral("X"), QStringLiteral("Y"), QStringLiteral("Z"),
                              QStringLiteral("Roll"), QStringLiteral("Pitch"), QStringLiteral("Yaw")};
    for (int i = 0; i < 6; ++i) {
        s[i] = new QDoubleSpinBox(&dlg);
        s[i]->setRange(-1.0e7, 1.0e7);
        s[i]->setDecimals(6);
        s[i]->setSingleStep(0.1);
        s[i]->setValue((*pose)[static_cast<std::size_t>(i)]);
        form->addRow(labels[i], s[i]);
    }
    layout->addLayout(form);
    auto* buttons = new QDialogButtonBox(QDialogButtonBox::Ok | QDialogButtonBox::Cancel, &dlg);
    QObject::connect(buttons, &QDialogButtonBox::accepted, &dlg, &QDialog::accept);
    QObject::connect(buttons, &QDialogButtonBox::rejected, &dlg, &QDialog::reject);
    layout->addWidget(buttons);
    if (dlg.exec() != QDialog::Accepted) {
        return false;
    }
    for (int i = 0; i < 6; ++i) {
        (*pose)[static_cast<std::size_t>(i)] = s[i]->value();
    }
    return true;
}

QString sanitizedModelName(QString name) {
    name = name.trimmed();
    name.replace(QRegularExpression(QStringLiteral("[^A-Za-z0-9_\\-]+")), QStringLiteral("_"));
    name.replace(QRegularExpression(QStringLiteral("_+")), QStringLiteral("_"));
    while (name.startsWith(QLatin1Char('_'))) {
        name.remove(0, 1);
    }
    while (name.endsWith(QLatin1Char('_'))) {
        name.chop(1);
    }
    if (name.isEmpty()) {
        name = QStringLiteral("imported_model");
    }
    return name;
}

QString buildModelConfigXml(const QString& model_name) {
    QString text;
    QTextStream out(&text);
    out << "<?xml version=\"1.0\"?>\n";
    out << "<model>\n";
    out << "  <name>" << model_name << "</name>\n";
    out << "  <version>1.0</version>\n";
    out << "  <sdf version=\"1.6\">model.sdf</sdf>\n";
    out << "  <author>\n";
    out << "    <name>EchoVerse Sonar Lab</name>\n";
    out << "    <email>noreply@echoverse.local</email>\n";
    out << "  </author>\n";
    out << "  <description>Imported model generated by Scene Editor.</description>\n";
    out << "</model>\n";
    return text;
}

QString buildModelSdfXml(const QString& model_name, const QString& mesh_file_name) {
    QString text;
    QTextStream out(&text);
    out << "<?xml version=\"1.0\" ?>\n";
    out << "<sdf version=\"1.6\">\n";
    out << "  <model name=\"" << model_name << "\">\n";
    out << "    <static>true</static>\n";
    out << "    <link name=\"link\">\n";
    out << "      <pose>0 0 0 0 0 0</pose>\n";
    out << "      <collision name=\"collision\">\n";
    out << "        <geometry>\n";
    out << "          <mesh>\n";
    out << "            <uri>model://" << model_name << "/meshs/" << mesh_file_name << "</uri>\n";
    out << "          </mesh>\n";
    out << "        </geometry>\n";
    out << "      </collision>\n";
    out << "      <visual name=\"visual\">\n";
    out << "        <geometry>\n";
    out << "          <mesh>\n";
    out << "            <uri>model://" << model_name << "/meshs/" << mesh_file_name << "</uri>\n";
    out << "          </mesh>\n";
    out << "        </geometry>\n";
    out << "      </visual>\n";
    out << "    </link>\n";
    out << "  </model>\n";
    out << "</sdf>\n";
    return text;
}

bool writeTextFileUtf8(const QString& file_path, const QString& content, QString* err) {
    QFile file(file_path);
    if (!file.open(QIODevice::WriteOnly | QIODevice::Truncate | QIODevice::Text)) {
        if (err) {
            *err = QStringLiteral("Failed to write file: %1").arg(file_path);
        }
        return false;
    }
    const QByteArray data = content.toUtf8();
    const qint64 written = file.write(data);
    file.close();
    if (written != data.size()) {
        if (err) {
            *err = QStringLiteral("Failed to write full file content: %1").arg(file_path);
        }
        return false;
    }
    return true;
}

